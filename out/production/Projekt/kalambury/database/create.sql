DROP TABLE IF EXISTS ranking CASCADE;
DROP TABLE IF EXISTS slowo CASCADE;
DROP TABLE IF EXISTS czas CASCADE;
DROP TABLE IF EXISTS gracze CASCADE;
DROP TABLE IF EXISTS obraz CASCADE;

CREATE TABLE ranking (
  id     SERIAL NOT NULL PRIMARY KEY,
  nazwa  TEXT   NOT NULL,
  punkty INT    NOT NULL
);

CREATE TABLE slowo (
  slowo TEXT
);

CREATE TABLE czas (
  czas DOUBLE PRECISION
);

CREATE TABLE obraz (
  x1 DOUBLE PRECISION,
  y1 DOUBLE PRECISION,
  x2 DOUBLE PRECISION,
  y2 DOUBLE PRECISION,
  START_OPACITY DOUBLE PRECISION,
  OPACITY_MODIFIER DOUBLE PRECISION,
  currentOpacity DOUBLE PRECISION,
  strokeWidth DOUBLE PRECISION,
  r int,
  g int,
  b int
);

CREATE TABLE gracze (
  name TEXT,
  ile_razy INT,
  rysuje INT
);
